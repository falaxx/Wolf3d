int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int nb;

	nb = 0;
	i = -1;
	while (tab[++i] != 0)
	{
		if (f(tab[i]) == 1)
			nb++;
	}
	return (nb);
}
