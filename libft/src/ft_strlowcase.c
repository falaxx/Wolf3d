char	*ft_strlowcase(char *str)
{
	int ct;

	ct = 0;
	while (str[ct] != '\0')
	{
		if (str[ct] >= 65 && str[ct] <= 90)
			str[ct] = str[ct] + 32;
		++ct;
	}
	return (str);
}
