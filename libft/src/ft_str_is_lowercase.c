int		ft_str_is_lowercase(char *str)
{
	int ct;

	ct = 0;
	while (str[ct] != '\0')
	{
		if (str[0] == '\0')
			return (1);
		if (str[ct] < 97 || str[ct] > 122)
			return (0);
		ct++;
	}
	return (1);
}
